                                    <h2 class="has-text-align-center">A Bit About Our Stickers &amp; The Process</h2>



                                    <p class="has-text-align-center" style="font-size:24px">Customer satisfaction is our number one goal, and to do that, we need to make sure your stickers come out exactly how you would expect them to.</p>



                                    <h3 class="has-text-color" style="color:#7b63a9">We always laminate</h3>



                                    <p>Nobody likes a thin fading sticker. We print our stickers using primum ink on quality thick vinyl and then laminate it! Competitors will skip out on this time consuming step of laminating and the use of quality vinyl.
                                        We believe if you are going to do something do it right 🙂 Our motto is Custom Stickers Done Right for a reason. </p>



                                    <h3 class="has-text-color" style="color:#7b63a9">Water-Resistant</h3>



                                    <p>The rain doesn’t scare us – these stickers are waterproof. Don’t be scared to put your water bottle in the dishwasher, either. Our stickers will last in harsh water environments.</p>



                                    <h3 class="has-text-color" style="color:#7b63a9">Eco-Friendly</h3>



                                    <p>We don’t use harsh chemicals, because our ink is water based and odor free. We maximize the space used in the cutting process to produce the least amount of waste possible. We take pride in recycling every scrap we
                                        can.</p>



                                    <h3 class="has-text-color" style="color:#7b63a9">Proofing</h3>



                                    <p>We want you to love your stickers. This is why we send proofs – we want to make sure you approve your design before we print. Customer satisfaction is our top priority.</p>



                                    <h3 class="has-text-color" style="color:#7b63a9">Shipped In <em>1 &#8211; 2 </em>Business Days</h3>



                                    <p>Once you approve the proof we send, we will have your stickers printed, cut, and shipped within 1-2 business day. We do everything we can to get you your stickers ASAP.</p>



                                    <h3 class="has-text-color" style="color:#7b63a9">Stickers Up To <em>48 X 48</em></h3>



                                    <p>Large prints aren’t a problem! We can print up to 48”x48”. The quality and resolution at this size is no different than a small sticker – however, please note that the quality of the sticker will depend on the quality
                                        of the file you send us.</p>
